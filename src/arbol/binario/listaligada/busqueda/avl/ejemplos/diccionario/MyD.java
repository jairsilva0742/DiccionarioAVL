    private void jButtonCargarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonCargarMouseClicked
        try {
            diccionario = new Diccionario(jTextAreaResultado);
            if (diccionario != null) {
                jLabelCantidad.setText(diccionario.getCantidad() + "");
            }
        } catch (IOException ex) {
            jTextAreaResultado.setText(ex.getLocalizedMessage());
        } catch (Exception ex) {
            Logger.getLogger(MyD.class.getName()).log(Level.SEVERE, null, ex);
        }
        jButtonCargar.setEnabled(false);
        jTextAreaResultado.setText("Finaliz√≥ la carga del diccionario");
    }//GEN-LAST:event_jButtonCargarMouseClicked

